#include<iostream>
#include<cmath>
using namespace std;
int main()
{
	cout<<"Max value  "<<fmax(80,100);
	cout<<endl;
	cout<<"Min value"<<fmin(30,40);
	cout<<endl;
	cout<<"Madulous  "<<fmod(80,100);
	cout<<endl;
	cout<<"Sin hyperbola"<<sinh(30);
	cout<<endl;
	cout<<"Exponent "<< exp(8);
	cout<<endl;
	cout<<"Sin"<<sin(30);
	cout<<endl;
	cout<<"cos  "<<cos(80);
	cout<<endl;
	cout<<"Losing precision value"<<fma(30,40,50);
	cout<<endl;
}
